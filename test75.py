# دالة البحث الثنائي
def binary_search(array, target):
    low = 0  # البداية
    high = len(array) - 1  # النهاية
    
    while low <= high:  # طالما أن هناك عناصر يجب البحث فيها
        mid = (low + high) // 2  # تحديد العنصر الأوسط
        
        # إذا كان العنصر الأوسط هو العنصر المطلوب
        if array[mid] == target:
            return f"تم العثور على العنصر في الموقع {mid}"
        
        # إذا كان العنصر المطلوب أقل من العنصر الأوسط، نبحث في النصف الأيسر
        elif array[mid] > target:
            high = mid - 1  # نقل نقطة النهاية
        # إذا كان العنصر المطلوب أكبر من العنصر الأوسط، نبحث في النصف الأيمن
        else:
            low = mid + 1  # نقل نقطة البداية
    
    return "العنصر غير موجود"  # إذا لم يتم العثور على العنصر

# مثال للاستخدام:
my_list = [10, 20, 30, 40, 50]  # قائمة مرتبة
target = 30  # العنصر المطلوب

# استدعاء دالة البحث الثنائي
result = binary_search(my_list, target)

# طباعة النتيجة
print(result)
