# دالة البحث الخطي
def linear_search(array, target):
    # المرور على كل عنصر في القائمة
    for i in range(len(array)):  # len(array) تعطي طول القائمة
        # التحقق إذا كان العنصر الحالي في القائمة يساوي العنصر المطلوب
        if array[i] == target:
            return f"تم العثور على العنصر في الموقع {i}"  # إرجاع الموقع إذا تم العثور على العنصر
    return "العنصر غير موجود"  # إرجاع رسالة إذا لم يتم العثور على العنصر

# مثال للاستخدام:
my_list = [10, 20, 30, 40, 50]  # القائمة
target = 30  # العنصر المطلوب

# استدعاء دالة البحث الخطي
result = linear_search(my_list, target)

# طباعة النتيجة
print(result)
