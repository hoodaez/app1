# 1. الأعداد الصحيحة (Integer)
# الأعداد الصحيحة هي أعداد بدون فاصلة عشرية.
int_num = 10  # مثال على عدد صحيح
print(f"Integer Example: {int_num}")  # طباعة العدد الصحيح

print("\n")

# 2. الأعداد العشرية (Float)
# الأعداد العشرية هي أعداد تحتوي على فاصلة عشرية.
float_num = 10.5  # مثال على عدد عشري
print(f"Float Example: {float_num}")  # طباعة العدد العشري

print("\n")

# 3. النصوص (String)
# النصوص هي مجموعة من الأحرف محاطة بعلامات اقتباس (مفردة أو مزدوجة).
str_example = "Hello, World!"  # مثال على نص
print(f"String Example: {str_example}")  # طباعة النص

print("\n")

# 4. القوائم (List)
# القوائم هي مجموعات مرتبة من العناصر (يمكن أن تكون متنوعة) ومرتبطة بأقواس مربعة [].
list_example = [1, 2, 3, "apple", 5.6]  # مثال على قائمة تحتوي على أعداد ونصوص
print(f"List Example: {list_example}")  # طباعة القائمة

print("\n")

# 5. القواميس (Dictionary)
# القواميس هي مجموعات غير مرتبة من الأزواج المفتاحية/القيم.
dict_example = {"name": "Ali", "age": 30, "city": "Cairo"}  # مثال على قاموس
print(f"Dictionary Example: {dict_example}")  # طباعة القاموس

print("\n")

# 6. المجموعات (Set)
# المجموعات هي مجموعات غير مرتبة من العناصر الفريدة.
set_example = {1, 2, 3, 4, 5}  # مثال على مجموعة تحتوي على أرقام
print(f"Set Example: {set_example}")  # طباعة المجموعة

print("\n")

# 7. القيم المنطقية (Boolean)
# القيم المنطقية تحتوي على قيمتين فقط: True أو False.
bool_example = True  # مثال على قيمة منطقية
print(f"Boolean Example: {bool_example}")  # طباعة القيمة المنطقية

print("\n")

# 8. القيم المجهولة (None)
# None هو نوع بيانات يُستخدم للإشارة إلى قيمة غير معرفة أو مفقودة.
none_example = None  # مثال على None
print(f"None Example: {none_example}")  # طباعة القيمة None
